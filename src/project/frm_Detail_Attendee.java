    //Method for Btn_Cal (it calculate ticket price by using method cal_Ticket from class Booking)
    private void btn_CalMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_btn_CalMouseClicked
    {//GEN-HEADEREND:event_btn_CalMouseClicked
        obj_Booking = new Booking();

        String seats = txt_Seats.getText();
        String ticket = txt_Ticket.getText();

        boolean bahrian;

        if (ck_Bahrian.isSelected())
        {
            bahrian = true;
        }
        else
        {
            bahrian = false;
        }

        int bill = obj_Booking.cal_Ticket(bahrian, seats, ticket);

        if (bill == 0)
        {
            txt_Seats.requestFocus();
            txt_Seats.setText("");
        }
        else
        {
            txt_Bill.setText(String.valueOf(bill));
        }
    }//GEN-LAST:event_btn_CalMouseClicked

