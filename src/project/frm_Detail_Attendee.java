    //Method for btn_Confirm (It adds data to DB tbl_Register by calling method ins_Attendee from class Booking)
    private void btn_ConfrimMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_btn_ConfrimMouseClicked
    {//GEN-HEADEREND:event_btn_ConfrimMouseClicked
        try
        {
            if (txt_Bill.getText().isEmpty() || txt_Bill.getText().equalsIgnoreCase("0"))
            {
                JOptionPane.showMessageDialog(this, "Please, Calculte price.");
            }

            else if (txt_Name.getText().isEmpty() || txt_Contact.getText().isEmpty() || txt_Seats.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(this, "Please, Enter in all fields");
            }
            else
            {
                boolean Success = obj_Booking.ins_Attendee(txt_Event.getText(), txt_Date.getText(), txt_Day.getText(), txt_Venue.getText(), txt_Name.getText(),
                                                           txt_Contact.getText(), txt_Seats.getText(), txt_Bill.getText());

                if (Success)
                {
                    JOptionPane.showMessageDialog(this, "Registration Successfull.");
                    obj_frm_Booking.setVisible(true);
                    this.dispose();
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Something went wrong.");
                    obj_frm_Booking.setVisible(true);
                    this.dispose();
                }
            }
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_ConfrimMouseClicked

